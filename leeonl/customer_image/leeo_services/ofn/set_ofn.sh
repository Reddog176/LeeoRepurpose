#OFN device address = 0x33
echo "set_ofn.................................."
#####Set PowerUp##### 
#Set OFN_Engine1 (0x60) to 0xE4
i2cset -y 1 0x33 0x60 0xE4

#Set OFN_Engine2 (0x61) to 0xC9
i2cset -y 1 0x33 0x61 0xC9

#####Enable OFN#####
#Set EVENT_CTRL (0x1D) to 0x8A
i2cset -y 1 0x33 0x1D 0x8A

#Set FPD_THRESH (0x76) to 0x05
i2cset -y 1 0x33 0x76 0x05

#Set MOTION_CTRL (0x74) to 0x01
i2cset -y 1 0x33 0x74 0x01

#Set FPD_CTRL (0x75) to 0x1A
i2cset -y 1 0x33 0x75 0x1A

#Set OFN_Engine1 (0x60) to 0xD4
i2cset -y 1 0x33 0x60 0xD4

#Set QUANTIZE_CTRL (0x72) to 0x80 to enable YQ only. Set to 0x08 to enable XQ only
i2cset -y 1 0x33 0x72 0x08

#Set XYQ_THRESH (0x73) to 0x02
i2cset -y 1 0x33 0x73 0x02

